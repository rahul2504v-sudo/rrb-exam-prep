def parse_ts_file(filepath):
    """Parse a TypeScript file with Question[] objects using regex."""
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    questions = []
    # Find all question objects between { and }
    # Match the question blocks
    pattern = r'\{\s*\n\s*id:\s*[\'"]([^\'"]+)[\'"],\s*\n\s*examId:\s*[\'"]([^\'"]+)[\'"],\s*\n\s*subjectId:\s*[\'"]([^\'"]+)[\'"],\s*\n\s*topicId:\s*[\'"]([^\'"]+)[\'"],\s*\n\s*questionText:\s*[\'"](.+?)[\'"],\s*\n\s*optionA:\s*[\'"](.+?)[\'"],\s*\n\s*optionB:\s*[\'"](.+?)[\'"],\s*\n\s*optionC:\s*[\'"](.+?)[\'"],\s*\n\s*optionD:\s*[\'"](.+?)[\'"],\s*\n\s*correctOption:\s*[\'"]([^\'"]+)[\'"],\s*\n\s*explanation:\s*[\'"](.+?)[\'"],\s*\n\s*difficulty:\s*[\'"]([^\'"]+)[\'"],\s*\n\s*sourceYear:\s*(\d+),?\s*\n\s*\}'
    
    matches = re.findall(pattern, content, re.DOTALL)
    for m in matches:
        q = {
            'oldId': m[0],
            'examId': m[1],
            'subjectId': m[2],
            'topicId': m[3],
            'questionText': m[4],
            'optionA': m[5],
            'optionB': m[6],
            'optionC': m[7],
            'optionD': m[8],
            'correctOption': m[9],
            'explanation': m[10],
            'difficulty': m[11],
            'sourceYear': int(m[12]),
        }
        questions.append(q)
    
    print(f"Parsed {len(questions)} questions from {os.path.basename(filepath)}")
    return questions

