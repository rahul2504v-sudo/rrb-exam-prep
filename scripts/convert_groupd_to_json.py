def build_json(questions, topic_map, prefix, hi_translations):
    """Group questions by topicId, assign new IDs, and create JSON."""
    # Group by topicId
    groups = {}
    for q in questions:
        tid = q['topicId']
        if tid not in groups:
            groups[tid] = []
        groups[tid].append(q)
    
    result = {}
    for tid, qs in sorted(groups.items()):
        slug = topic_map.get(tid, tid.split('-')[-1])
        json_questions = []
        for idx, q in enumerate(qs, 1):
            new_id = f"{prefix}{idx:04d}"
            old_id = q['oldId']
            hi = hi_translations.get(old_id, {})
            
            json_q = {
                'id': new_id,
                'correctOption': q['correctOption'],
                'difficulty': q['difficulty'],
                'sourceYear': q['sourceYear'],
                'en': {
                    'question': q['questionText'],
                    'options': [q['optionA'], q['optionB'], q['optionC'], q['optionD']],
                    'explanation': q['explanation'],
                },
                'hi': {
                    'question': hi.get('question', q['questionText']),
                    'options': hi.get('options', [q['optionA'], q['optionB'], q['optionC'], q['optionD']]),
                    'explanation': hi.get('explanation', q['explanation']),
                }
            }
            json_questions.append(json_q)
        
        result[slug] = json_questions
    
    return result

